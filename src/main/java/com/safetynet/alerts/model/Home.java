package com.safetynet.alerts.model;

public class Home {

  private int id;
  private int homeNumber;
  private String street;
  private String city;
  private String state;
  private String postalCode;
  private int idFirestation;

  public int getId() {
    return id;
  }

  public void setId(int id) {
    this.id = id;
  }

  public int getHomeNumber() {
    return homeNumber;
  }

  public void setHomeNumber(int homeNumber) {
    this.homeNumber = homeNumber;
  }

  public String getStreet() {
    return street;
  }

  public void setStreet(String street) {
    this.street = street;
  }

  public String getCity() {
    return city;
  }

  public void setCity(String city) {
    this.city = city;
  }

  public String getState() {
    return state;
  }

  public void setState(String state) {
    this.state = state;
  }

  public String getPostalCode() {
    return postalCode;
  }

  public void setPostalCode(String postalCode) {
    this.postalCode = postalCode;
  }

  public int getIdFirestation() {
    return idFirestation;
  }

  public void setIdFirestation(int idFirestation) {
    this.idFirestation = idFirestation;
  }

  @Override
  public String toString() {
    return String.format(
        "Person [id=%s, HomeNumber=%s, Street=%s, City=%s, State=%s, ZipCode=%s, FirestationId=%s]",
        id, homeNumber, street, city, state, postalCode, idFirestation);
  }
}
